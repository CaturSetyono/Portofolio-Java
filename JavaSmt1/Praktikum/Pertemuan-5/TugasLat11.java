public class TugasLat11{
    public static void main(String[] args) {
        // Loop for
         for (int baris=1; baris <=5; baris++){
		        for (int i = 1; i <=baris; i++) {
					System.out.print(" "+i);
				}
		            System.out.println();
	}
        for (int baris=4; baris >=1; baris--){
        		for (int i = 1; i <=baris; i++) {
					System.out.print(" "+i);
				}
            System.out.println();
	}
    }
}